#include<iostream>
using namespace std;
int main()
{
	cout << "char length   is   " << sizeof(char) << endl;
	cout << "int length    is   " << sizeof(int) << endl;
	cout << "float length  is   " << sizeof(float) << endl;
	cout << "double length is   " << sizeof(double) << endl;
	return 0;
}